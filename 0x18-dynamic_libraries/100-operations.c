#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: final result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two integers
 * @a: first int
 * @b: second int
 * Return: subtracted result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two integers
 * @a: first int
 * @b: second int
 * Return: multiplied result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two integers
 * @a: first int
 * @b: second int
 * Return: divided result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: final result
 */

int mod(int a, int b)
{
	return (a % b);
}
